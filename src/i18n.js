const SUPPORTED_LANGUAGE_CODES = ["zh-CN", "en"];

function formatMessage(template, values = {}) {
  return String(template || "").replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) =>
    Object.hasOwn(values, key) ? String(values[key]) : match
  );
}

function normalizeLanguage(value) {
  const normalized = String(value || "").trim().toLowerCase();
  if (normalized.startsWith("zh")) {
    return "zh-CN";
  }
  return "en";
}

function getBrowserLanguage() {
  const i18nLanguage = globalThis.chrome?.i18n?.getUILanguage?.();
  return normalizeLanguage(i18nLanguage || globalThis.navigator?.language || "en");
}

function resolveUiLanguage(settings = {}) {
  const value = String(settings.uiLanguage || "auto");
  return value === "auto" ? getBrowserLanguage() : normalizeLanguage(value);
}

function resolveOutputLanguage(settings = {}) {
  const value = String(settings.outputLanguage || "auto");
  if (value === "auto") {
    return resolveUiLanguage(settings);
  }
  return normalizeLanguage(value);
}

function createTranslator(language) {
  const locale = SUPPORTED_LANGUAGE_CODES.includes(language) ? language : "en";
  const fallback = MIT_STUDY_MESSAGES.en;
  const messages = MIT_STUDY_MESSAGES[locale] || fallback;
  return function translate(key, values = {}) {
    const template = messages[key] ?? fallback[key] ?? MIT_STUDY_MESSAGES["zh-CN"][key] ?? key;
    return formatMessage(template, values);
  };
}

globalThis.MitStudyI18n = {
  MIT_STUDY_MESSAGES,
  SUPPORTED_LANGUAGE_CODES,
  createTranslator,
  formatMessage,
  getBrowserLanguage,
  normalizeLanguage,
  resolveOutputLanguage,
  resolveUiLanguage
};
